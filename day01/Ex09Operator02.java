package day01;

public class Ex09Operator02 {
	public static void main(String[] args) {
		int number = 5;
		System.out.println("number의 현재값 : " +number);
		number +=4 ;
		System.out.println("number의 현재값 : " +number);
		number -=3 ;
		System.out.println("number의 현재값 : " +number);
		number *=4 ;
		System.out.println("number의 현재값 : " +number);
		number /=3 ;
		System.out.println("number의 현재값 : " +number);
		number %=5 ;
		System.out.println("number의 현재값 : " +number);
		
		
	}
}
